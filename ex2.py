i = int(input("Введите годовую процентную ставку: "))
s = int(input("Введите сумму займа: "))
n = int(input("Введите количество месяцев: "))

p = i/100/12

m = ((s * p * (1 + p) ** n)) / (((1 + p) ** n) - 1)

print("Ежемесячный платеж = ", m, "руб.")
print("Общая сумма выплат = ", (m*n), "руб.")
print("Переплата составит = ", m*n-s, "руб.")